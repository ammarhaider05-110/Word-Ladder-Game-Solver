// Purpose: maekes a deep copy of the ladder 
// Parameters: takes in the ladder
// Returns: pointr ot the head of the copied ladder
WordNode* copyLadder(WordNode* ladder) {
    //---------------------------------------------------------
    // TODO - write copyLadder()
    //---------------------------------------------------------
    WordNode* newLadder = NULL;
    WordNode* lastNode = NULL;
    WordNode* curr = ladder;
    while(curr != NULL){
        WordNode* newNode = malloc(sizeof(WordNode));
        newNode->myWord = curr->myWord;
        newNode->next = NULL;

        if(newLadder == NULL){
            newLadder = newNode;
        } else {
            lastNode->next = newNode;
        }

        lastNode = newNode;
        curr = curr->next;

    }
    return newLadder; //modify this line
}

