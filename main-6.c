// Purpose: counts the number fo difference between two strings
// Parameters: takes in the str1 and str2
// Returns: the number of differences 
int strCmpCnt(char* str1, char* str2) {
    //---------------------------------------------------------
    // TODO - write strCmpCnt()
    //---------------------------------------------------------
    int count = 0;

    while(*str1 != '\0' && *str2 != '\0'){
        if(*str1 != *str2){
            count++;
        }
        str1++;
        str2++;
    }

    while(*str1 != '\0'){
        str1++;
        count++;
    }
    while(*str2 != '\0'){
        str2++;
        count++;
    }
    
    return count; //modify this line
}

// Purpose: looks for the first index that a difference between two string occures
// Parameters: takes in str1 and str2 
// Returns: the index of the first difference
int strCmpInd(char* str1, char* str2) {
    //---------------------------------------------------------
    // TODO - write strCmpInd()
    //---------------------------------------------------------
    int ind = 0;
    while(*str1 != '\0' && *str2 != '\0'){
        if(*str1 != *str2){
            return ind;
        }
        ind++;
        str1++;
        str2++;
    }
    if(*str1 != *str2){
        return ind;
    }

    return -1; //modify this line
}

