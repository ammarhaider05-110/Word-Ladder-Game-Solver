// Purpose: searchs for a word in an array using binary search
// Parameters: takes in words, aword, loind, and hiind
// Returns: the index of the word in the array and -1 if the word is not in the array
int findWord(char** words, char* aWord, int loInd, int hiInd) {
    //---------------------------------------------------------
    // TODO - write findWord()
    //---------------------------------------------------------
    int mid = (hiInd + loInd) / 2;
    int find = strcmp(words[mid], aWord);
    if (loInd > hiInd){
        return -1;
    } else if (find == 0){
        return mid;
    } else if (find < 0){
        return findWord(words, aWord, mid + 1, hiInd);
    } else if (find > 0){
        return findWord(words, aWord, loInd, mid - 1);
    }
}

