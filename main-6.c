// Purpose: This functions counts how many words are in the file with the length givene
// Parameters: takes in filenmae, and the wordsize
// Returns: the number of words withthe correct size and -1 if it doestn work
int countWordsOfLength(char* filename, int wordSize) { 
    //---------------------------------------------------------
    // TODO - write countWordsOfLength()    
    //---------------------------------------------------------
    int wordCount = 0;
    char word[256];
    FILE* fp = fopen(filename, "r");
    if (!fp){
        return -1;
    }
    while (fgets(word, sizeof(word), fp) != NULL){
        word[strcspn(word, "\n")] = '\0';
        if(strlen(word) == wordSize){
            wordCount++;
        }
    }
    fclose(fp);
    return wordCount; //modify this line
}

