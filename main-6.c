// Purpose: adds a ladder to the end of the queue
// Parameters: takes in the list and the newLadder
// Returns: nothign its void
void insertLadderAtBack(LadderNode** list, WordNode* newLadder) {
    //---------------------------------------------------------
    // TODO - write insertLadderAtBack()
    //---------------------------------------------------------
    LadderNode* newLadderNode = malloc(sizeof(LadderNode));
    newLadderNode->topWord = newLadder;
    newLadderNode->next = NULL;
    LadderNode* curr = *list;

    if(*list == NULL){
        *list = newLadderNode;
        return;
    }
    while(curr->next != NULL){
        curr = curr->next;
    }
    curr->next = newLadderNode;

}

// Purpose: removes the front ladder of the queue
// Parameters: takes int the list
// Returns: the node representting the ladder that was removed
WordNode* popLadderFromFront(LadderNode** list) {
    //---------------------------------------------------------
    // TODO - write popLadderFromFront()
    //---------------------------------------------------------
    if(*list == NULL){
        return NULL;
    }
    LadderNode* head = *list;
    WordNode* ladder = head->topWord;
    *list = head->next;
    free(head);

    return ladder; //modify this line
}

